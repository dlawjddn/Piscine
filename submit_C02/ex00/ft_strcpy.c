char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	if (src == '\0')
		return (0);
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
